    @staticmethod
    def makeQuizAi(count_questions, theme, prev_questions: list) -> dict:
        content_sys = QuizAi.role_quiz_maker % (theme, count_questions)

        messages = [{"role": "system", "content": content_sys}]

        if len(prev_questions) > 0:
            restricted_questions_title = "‚ö†Ô∏è –ù–µ –∏—Å–ø–æ–ª—å–∑—É–π –≤ –≤–∏–∫—Ç–æ—Ä–∏–Ω–µ —ç—Ç–∏ –≤–æ–ø—Ä–æ—Å—ã:\n"
            max_count = 60
            count = 0

            part_of_prompt = {"role": "user", "content": restricted_questions_title}

            for question in prev_questions:
                part_of_prompt['content'] += f"- {question}\n"
                count += 1
                if count == max_count:
                    messages.append(part_of_prompt)
                    part_of_prompt = {"role": "user", "content": restricted_questions_title}
                    count = 0

            if count > 0:
                messages.append(part_of_prompt)
        
        # logging.info(f"–í–æ—Ç —Å–æ–æ–±—â–µ–Ω–∏—è: {messages}")
                
        response = openai.chat.completions.create(
            model="gpt-4.1-mini",
            messages=messages
        )

        quiz_text = response.choices[0].message.content

        return QuizAi.parse_quiz(quiz_text, theme)

    def parse_quiz(quiz_text: str, theme):
        lines = quiz_text.strip().split("\n")
        questions_answers = {theme: {}}
        current_question = None
        current_number = None
        quiz_title = ""

        questions = []

        for line in lines:
            line = line.strip()

            # –û–±—Ä–∞–±–æ—Ç–∫–∞ –∑–∞–≥–æ–ª–æ–≤–∫–∞
            if line.startswith("–ó–∞–≥–æ–ª–æ–≤–æ–∫ —Ç–µ—Å—Ç–∞:"):
                quiz_title = re.sub(r"^–ó–∞–≥–æ–ª–æ–≤–æ–∫ —Ç–µ—Å—Ç–∞:\s*", "", line)
                continue

            # –û–±—Ä–∞–±–æ—Ç–∫–∞ —Å—Ç—Ä–æ–∫–∏ —Å –≤–æ–ø—Ä–æ—Å–æ–º: –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å —Ü–∏—Ñ—Ä—ã –∏ —Ç–æ—á–∫–∏
            question_match = re.match(r"^(\d+)\.\s+(.+)", line)
            if question_match:
                current_number = int(question_match.group(1))
                current_question = question_match.group(2).strip()
                questions.append(current_question)
                key = f"{current_number}. {current_question}"
                questions_answers[theme][key] = {}
                continue

            # –û–±—Ä–∞–±–æ—Ç–∫–∞ —Å—Ç—Ä–æ–∫–∏ —Å –æ—Ç–≤–µ—Ç–æ–º: "- —Ç–µ–∫—Å—Ç (‚úÖ/‚ùå)"
            answer_match = re.match(r"^- (.+) \((‚úÖ|‚ùå)\)", line)
            if answer_match and current_question:
                answer_text = answer_match.group(1).strip()
                is_correct = answer_match.group(2) == "‚úÖ"
                key = f"{current_number}. {current_question}"
                questions_answers[theme][key][answer_text] = is_correct

        return quiz_title, questions_answers