    @staticmethod
    def askBibleChat(user_msg: str, context_msgs: list, user_name):
        context_role = BibleChatAi.role_bot % user_name

        if len(context_msgs) > 1:
            context_role += "\n**–í–æ—Ç –ø—Ä–µ–¥—ã–¥—É—â–∏–µ —Å–æ–æ–±—â–µ–Ω–∏—è**:\n"
            for context_msg in context_msgs:
                cur_msg = f'- –¢–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ: "{context_msg.text}"'
                if not context_msg.is_bot:
                    cur_msg = cur_msg.replace("–¢–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ", "–°–æ–æ–±—â–µ–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è", 1)
                context_role += cur_msg + "\n"    

        response = openai.chat.completions.create(
            model="gpt-4o-mini",
            messages=[
                {"role": "system", "content": context_role},
                {"role": "user", "content": user_msg}
            ]
        )
        ai_answer = response.choices[0].message.content
        return BibleChatAi.format_bible_answer(ai_answer)
    
    @staticmethod
    def format_bible_answer(text):
        # –ó–∞–º–µ–Ω—è–µ–º –∑–≤–µ–∑–¥–æ—á–∫–∏ –Ω–∞ —Ç–µ–≥–∏ <strong>
        text = re.sub(r'\*\*(.*?)\*\*', r'<strong>\1</strong>', text)
        
        # –ó–∞–º–µ–Ω—è–µ–º –∫–∞–∂–¥—ã–π --–ê–±–∑–∞—Ü-- –Ω–∞ <p> –∏ –∑–∞–∫–ª—é—á–∞–µ–º –∫–∞–∂–¥—ã–π –∞–±–∑–∞—Ü –≤ —Ç–µ–≥ <p></p>
        paragraphs = text.split('--–ê–±–∑–∞—Ü--')
        
        # –û—Ñ–æ—Ä–º–ª—è–µ–º –∫–∞–∂–¥—ã–π –∞–±–∑–∞—Ü –≤ <p></p> –∏ —É–±–∏—Ä–∞–µ–º –ª–∏—à–Ω–∏–µ –ø—Ä–æ–±–µ–ª—ã
        paragraphs = ['<p>' + para.strip() + '</p>' for para in paragraphs if para.strip()]
        
        # –°–∫–ª–µ–∏–≤–∞–µ–º –≤—Å–µ –∞–±–∑–∞—Ü—ã –æ–±—Ä–∞—Ç–Ω–æ –≤ –æ–¥–∏–Ω —Ç–µ–∫—Å—Ç
        return '\n\n'.join(paragraphs)
    
    @staticmethod
    def setTitleChat(user_msg):
        response = openai.chat.completions.create(
            model="gpt-4o-mini",
            messages=[
                {"role": "system", "content": BibleChatAi.role_namer_bot},
                {"role": "user", "content": user_msg}
            ]
        )
        return response.choices[0].message.content
    
    @staticmethod
    def getDailyVerse():
        response = openai.chat.completions.create(
            model="gpt-4o-mini",
            messages=[
                {"role": "system", "content": BibleChatAi.role_daily_verser}
            ]
        )
        response_text = response.choices[0].message.content
        parts = response_text.split("\n")

        verse, verse_title = None, None

        for part in parts:
            parts_of_part = part.split(': ')
            if not verse:
                verse = parts_of_part[1]
            elif not verse_title:
                verse_title = parts_of_part[1]

        return verse.strip(), verse_title
    
